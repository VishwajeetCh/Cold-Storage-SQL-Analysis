USE ColdStoragedb;

-- INSITES 1: Capacity Utilization (Units > 80% Full) --
   -- Capacity--
 SELECT 
    su.UnitID,
    su.Capacity,
    SUM(se.Quantity) AS TotalStored,
    (SUM(se.Quantity) / su.Capacity) * 100.0 AS UtilizationPercentage
FROM StorageUnits su
JOIN StorageEntries se ON su.UnitID = se.UnitID
GROUP BY su.UnitID, su.Capacity
ORDER BY UtilizationPercentage DESC;
 

-- INSITES 2: Expiry Risk Detection
SELECT 
    se.EntryID,
    se.ProductID,
    p.ProductName,
    p.MaxStorageDays,
    DATEDIFF(se.ExitDate, se.EntryDate) AS StoredDays
FROM StorageEntries se
JOIN Products p ON se.ProductID = p.ProductID
WHERE DATEDIFF(se.ExitDate, se.EntryDate) > p.MaxStorageDays;

  
-- INSITES 3: Top 5 Revenue Generating Farmers --
SELECT 
    p.FarmerID,
    f.name,
    SUM(p.Amount) AS TotalRevenue
FROM Payments p
JOIN farmers f
ON f.FarmerID = p.FarmerID
WHERE Status = 'Paid'
GROUP BY FarmerID
ORDER BY TotalRevenue DESC
LIMIT 5;


-- INSITES 4: Most Stored Product Category --

SELECT 
    p.Category,
    SUM(se.Quantity) AS TotalQuantity
FROM StorageEntries se
JOIN Products p ON se.ProductID = p.ProductID
GROUP BY p.Category
ORDER BY TotalQuantity DESC;


-- INSITES 5: Monthly Revenue Report (2026)

SELECT  
    date_format(PaymentDate, "%Y-%m") AS Month,
    SUM(Amount) AS MonthlyRevenue
FROM Payments
WHERE Status = 'Paid'
GROUP BY MONTH
ORDER BY Month;



